#include <unistd.h>

void    d_int(int i)
{
    i = '9';
    while (i >= '0')
    {
        write(1, &i, 1);
        i--;
    }
    write(1,"\n", 1);

}
int     main(void)
{
    int i;

    d_int(i);
}